// ── Core API ──────────────────────────────────────────────────────────────────

/** Open OS folder-picker dialog; returns selected path or null */
export async function openWorkspaceDialog(): Promise<string | null> {
  if (!IS_TAURI) return null
  const { open } = await import('@tauri-apps/plugin-dialog')
  const result = await open({ directory: true, multiple: false, title: 'Выберите папку рабочего пространства' })
  return typeof result === 'string' ? result : null
}

/** Read beresta.json from workspace root → Product[] */
export async function readBerestaJson(rootPath: string): Promise<Product[] | null> {
  if (!IS_TAURI) return null
  const raw = await readText(join(rootPath, 'beresta.json'))
  if (!raw) return null
  try {
    const data = JSON.parse(raw) as BerestaJson
    return data.products ?? null
  } catch {
    return null
  }
}

/** Write products to beresta.json */
export async function writeBerestaJson(rootPath: string, products: Product[]): Promise<void> {
  if (!IS_TAURI) return
  const payload: BerestaJson = { version: '1', products }
  await writeText(join(rootPath, 'beresta.json'), JSON.stringify(payload, null, 2))
}

/**
 * Ensure all folders and stub markdown files exist for the given product tree.
 * Safe to call on every save — only creates what's missing.
 */
export async function ensureWorkspaceStructure(rootPath: string, products: Product[]): Promise<void> {
  if (!IS_TAURI) return

  // DESIGN.md
  const designMdPath = join(rootPath, 'DESIGN.md')
  if (!(await fileExists(designMdPath))) {
    await writeText(designMdPath, defaultDesignMd())
  }

  for (const product of products) {
    const productDir = join(rootPath, 'projects', product.id)
    await ensureDir(productDir)

    const projectMdPath = join(productDir, 'project.md')
    if (!(await fileExists(projectMdPath))) {
      await writeText(projectMdPath, defaultProjectMd(product.title))
    }

    for (const screen of product.screens) {
      const screenDir = join(productDir, 'screens', screen.id)
      await ensureDir(screenDir)

      const screenMdPath = join(screenDir, 'screen.md')
      if (!(await fileExists(screenMdPath))) {
        await writeText(screenMdPath, defaultScreenMd(screen.title))
      }

      for (const feature of screen.features) {
        const featureDir = join(screenDir, 'features', feature.id)
        await ensureDir(featureDir)
      }
    }
  }
}

/** Read DESIGN.md */
export async function readDesignMd(rootPath: string): Promise<string> {
  if (!IS_TAURI) return ''
  return (await readText(join(rootPath, 'DESIGN.md'))) ?? ''
}

/** Write DESIGN.md */
export async function writeDesignMd(rootPath: string, content: string): Promise<void> {
  if (!IS_TAURI) return
  await writeText(join(rootPath, 'DESIGN.md'), content)
}

/** Read project.md for a product */
export async function readProjectMd(rootPath: string, productId: string): Promise<string> {
  if (!IS_TAURI) return ''
  return (await readText(join(rootPath, 'projects', productId, 'project.md'))) ?? ''
}

/** Write project.md */
export async function writeProjectMd(rootPath: string, productId: string, content: string): Promise<void> {
  if (!IS_TAURI) return
  await ensureDir(join(rootPath, 'projects', productId))
  await writeText(join(rootPath, 'projects', productId, 'project.md'), content)
}

/** Read screen.md */
export async function readScreenMd(rootPath: string, productId: string, screenId: string): Promise<string> {
  if (!IS_TAURI) return ''
  return (await readText(join(rootPath, 'projects', productId, 'screens', screenId, 'screen.md'))) ?? ''
}

/** Write screen.md */
export async function writeScreenMd(rootPath: string, productId: string, screenId: string, content: string): Promise<void> {
  if (!IS_TAURI) return
  await ensureDir(join(rootPath, 'projects', productId, 'screens', screenId))
  await writeText(join(rootPath, 'projects', productId, 'screens', screenId, 'screen.md'), content)
}

/** Read a feature file (analytics.md, concept.tsx, discussion.md, handoff.md) */
export async function readFeatureFile(
  rootPath: string, productId: string, screenId: string, featureId: string, file: FeatureFile,
): Promise<string> {
  if (!IS_TAURI) return ''
  return (
    await readText(join(rootPath, 'projects', productId, 'screens', screenId, 'features', featureId, file))
  ) ?? ''
}

/** Write a feature file */
export async function writeFeatureFile(
  rootPath: string, productId: string, screenId: string, featureId: string, file: FeatureFile, content: string,
): Promise<void> {
  if (!IS_TAURI) return
  const dir = join(rootPath, 'projects', productId, 'screens', screenId, 'features', featureId)
  await ensureDir(dir)
  await writeText(join(dir, file), content)
}

// ── Composable (reactive wrapper) ─────────────────────────────────────────────

export function useWorkspaceFs() {
  return {
    workspacePath,
    isDesktop: IS_TAURI,
    openWorkspaceDialog,
    readBerestaJson,
    writeBerestaJson,
    ensureWorkspaceStructure,
    readDesignMd,
    writeDesignMd,
    readProjectMd,
    writeProjectMd,
    readScreenMd,
    writeScreenMd,
    readFeatureFile,
    writeFeatureFile,
  }
}
