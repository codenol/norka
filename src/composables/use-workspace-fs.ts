/**
 * Береста Workspace FS
 *
 * Manages the on-disk project workspace:
 *
 *   <root>/
 *   ├── norka.json           ← full project tree (IDs, titles, completedSteps)
 *   ├── DESIGN.md              ← Google Stitch–compatible design system doc
 *   ├── library/components/    ← per-component markdown
 *   └── projects/
 *       └── <productId>/
 *           ├── project.md     ← product context for LLM
 *           └── screens/
 *               └── <screenId>/
 *                   ├── screen.md
 *                   └── features/
 *                       └── <featureId>/
 *                           ├── analytics.md
 *                           ├── concept.tsx
 *                           ├── discussion.md
 *                           └── handoff.md
 *
 * All Tauri APIs are dynamically imported so the module safely loads in
 * the browser (IS_TAURI will be false and every operation is a no-op).
 */

