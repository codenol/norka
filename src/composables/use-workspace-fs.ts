/** Open OS folder-picker dialog; returns selected path or null */
export async function openWorkspaceDialog(): Promise<string | null> {
  if (!IS_TAURI) return null
  const { open } = await import('@tauri-apps/plugin-dialog')
  const result = await open({
    directory: true,
    multiple: false,
    title: 'Выберите папку рабочего пространства'
  })
  return typeof result === 'string' ? result : null
}

/** Read norka.json from workspace root → Product[] */
export async function readNorkaJson(rootPath: string): Promise<Product[] | null> {
  if (!IS_TAURI) return null
  const raw = await readText(join(rootPath, 'norka.json'))
  if (!raw) return null
  try {
    const data = JSON.parse(raw) as NorkaJson
    return data.products
  } catch {
    return null
  }
}

/** Write products to norka.json */
export async function writeNorkaJson(rootPath: string, products: Product[]): Promise<void> {
  if (!IS_TAURI) return
  const payload: NorkaJson = { version: '1', products }
  await writeText(join(rootPath, 'norka.json'), JSON.stringify(payload, null, 2))
}

/**
 * Ensure all folders and stub markdown files exist for the given product tree.
 * Safe to call on every save — only creates what's missing.
 */
export async function ensureWorkspaceStructure(
  rootPath: string,
  products: Product[]
): Promise<void> {
  if (!IS_TAURI) return

  // DESIGN.md
  const designMdPath = join(rootPath, 'DESIGN.md')
  if (!(await fileExists(designMdPath))) {
    await writeText(designMdPath, defaultDesignMd())
  }

  for (const product of products) {
    const productDir = join(rootPath, 'projects', product.id)
    await ensureDir(productDir)

    const projectMdPath = join(productDir, 'project.md')
    if (!(await fileExists(projectMdPath))) {
      await writeText(projectMdPath, defaultProjectMd(product.title))
    }

    for (const screen of product.screens) {
      const screenDir = join(productDir, 'screens', screen.id)
      await ensureDir(screenDir)

      const screenMdPath = join(screenDir, 'screen.md')
      if (!(await fileExists(screenMdPath))) {
        await writeText(screenMdPath, defaultScreenMd(screen.title))
      }

      for (const feature of screen.features) {
        const featureDir = join(screenDir, 'features', feature.id)
        await ensureDir(featureDir)
        const defaults: Array<[FeatureFile, string]> = [
          [
            'feature-meta.json',
            JSON.stringify(
              {
                jiraIssueKey: feature.jiraIssueKey ?? '',
                jiraUrl: feature.jiraUrl ?? '',
                designerFullName: feature.designerFullName ?? '',
                confluenceUrl: feature.confluenceUrl ?? ''
              },
              null,
              2
            )
          ],
          ['analytics.md', '# Аналитика\n\n<!-- Заполните аналитический бриф -->\n'],
          [
            'analytics-source.md',
            '# Analytics Source\n\n<!-- Paste source analytics from Confluence -->\n'
          ],
          ['comments.json', JSON.stringify([], null, 2)],
          [
            'versions.json',
            JSON.stringify(
              [
                {
                  id: 'v1',
                  title: 'v1',
                  createdAt: new Date().toISOString(),
                  notes: 'Initial version'
                }
              ],
              null,
              2
            )
          ]
        ]
        for (const [file, content] of defaults) {
          const filePath = join(featureDir, file)
          if (!(await fileExists(filePath))) {
            await writeText(filePath, content)
          }
        }
      }
    }
  }
}

