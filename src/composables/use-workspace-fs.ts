/**
 * Береста Workspace FS
 *
 * Manages the on-disk project workspace:
 *
 *   <root>/
 *   ├── beresta.json           ← full project tree (IDs, titles, completedSteps)
 *   ├── DESIGN.md              ← Google Stitch–compatible design system doc
 *   ├── library/components/    ← per-component markdown
 *   └── projects/
 *       └── <productId>/
 *           ├── project.md     ← product context for LLM
 *           └── screens/
 *               └── <screenId>/
 *                   ├── screen.md
 *                   └── features/
 *                       └── <featureId>/
 *                           ├── analytics.md
 *                           ├── concept.tsx
 *                           ├── discussion.md
 *                           └── handoff.md
 *
 * All Tauri APIs are dynamically imported so the module safely loads in
 * the browser (IS_TAURI will be false and every operation is a no-op).
 */

import { ref, watch } from 'vue'
import { IS_TAURI } from '@/constants'
import type { Product } from './use-projects'

// ── Types ─────────────────────────────────────────────────────────────────────

export type FeatureFile = 'analytics.md' | 'concept.tsx' | 'discussion.md' | 'handoff.md'

interface BerestaJson {
  version: '1'
  products: Product[]
}

// ── Singleton workspace path ───────────────────────────────────────────────────

const LS_WORKSPACE = 'bereста:workspacePath'

export const workspacePath = ref<string | null>(localStorage.getItem(LS_WORKSPACE))

watch(workspacePath, (v) => {
  if (v) localStorage.setItem(LS_WORKSPACE, v)
  else localStorage.removeItem(LS_WORKSPACE)
})

// ── Helpers ───────────────────────────────────────────────────────────────────

function join(...parts: string[]): string {
  return parts.join('/').replace(/\/+/g, '/')
}

async function ensureDir(path: string): Promise<void> {
  const { mkdir } = await import('@tauri-apps/plugin-fs')
  await mkdir(path, { recursive: true })
}

async function fileExists(path: string): Promise<boolean> {
  try {
    const { stat } = await import('@tauri-apps/plugin-fs')
    await stat(path)
    return true
  } catch {
    return false
  }
}

async function readText(path: string): Promise<string | null> {
  try {
    const { readTextFile } = await import('@tauri-apps/plugin-fs')
    return await readTextFile(path)
  } catch {
    return null
  }
}

async function writeText(path: string, content: string): Promise<void> {
  const { writeTextFile } = await import('@tauri-apps/plugin-fs')
  await writeTextFile(path, content)
}

