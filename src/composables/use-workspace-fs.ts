/**
 * Read all component spec files, extract "## Правила" sections and return
 * a combined markdown string suitable for injecting into an AI system prompt.
 * Returns '' if the directory doesn't exist or no rules are found.
 */
export async function readAllComponentRules(rootPath: string): Promise<string> {
  if (!IS_TAURI) return ''
  try {
    const { readDir } = await import('@tauri-apps/plugin-fs')
    const dir = join(rootPath, 'library', 'components')
    const entries = await readDir(dir)
    const parts: string[] = []
    for (const entry of entries) {
      if (!entry.name.endsWith('.md')) continue
      const md = await readText(join(dir, entry.name))
      if (!md) continue
      // Extract component name from first line: # ComponentName
      const nameMatch = md.match(/^#\s+(.+)/m)
      const compName = nameMatch ? nameMatch[1].trim() : entry.name.replace('.md', '')
      // Extract ## Правила section
      const rulesMatch = md.match(/## Правила\s*\n([\s\S]*?)(?=\n## |$)/)
      if (rulesMatch?.[1].trim()) {
        parts.push(`## ${compName}\n${rulesMatch[1].trim()}`)
      }
    }
    return parts.length > 0 ? parts.join('\n\n') : ''
  } catch {
    return ''
  }
}

/** Minimal component spec needed for code export */
export interface CoreComponentSpec {
  id: string
  name: string
  primeReactImport: string
  primeReactExport: string
  description: string
  rules: string
}

function defaultPreviewPropsFor(name: string): Record<string, unknown> {
  switch (name) {
    case 'Button':
      return { label: 'Button', severity: 'primary' }
    case 'InputText':
      return { placeholder: 'Type here' }
    case 'Card':
      return { title: 'Card title', subTitle: 'Subtitle', children: 'Card content' }
    case 'Dialog':
      return { visible: true, header: 'Dialog', modal: false, children: 'Dialog content' }
    case 'Badge':
      return { value: 'Badge', severity: 'info' }
    case 'Avatar':
      return { label: 'AB', shape: 'circle' }
    case 'TabView':
      return { tabs: [{ header: 'Tab 1', content: 'Tab content' }] }
    case 'Dropdown':
      return {
        options: [
          { label: 'Option 1', value: '1' },
          { label: 'Option 2', value: '2' }
        ],
        optionLabel: 'label',
        placeholder: 'Select'
      }
    default:
      return {}
  }
}

function buildCoreRuntimeManifest(components: CoreComponentSpec[]): CoreRuntimeManifest {
  return {
    version: '1',
    generatedAt: new Date().toISOString(),
    components: components.map((comp) => ({
      id: comp.id,
      name: comp.name,
      importPath: comp.primeReactImport,
      exportName: comp.primeReactExport,
      description: comp.description,
      rules: comp.rules,
      previewDefaults: defaultPreviewPropsFor(comp.primeReactExport)
    }))
  }
}

export function validateCoreRuntimeManifest(
  manifest: CoreRuntimeManifest
): { ok: true } | { ok: false; errors: string[] } {
  const errors: string[] = []
  if (manifest.version !== '1') errors.push('Unsupported manifest version')
  if (!Array.isArray(manifest.components) || manifest.components.length === 0) {
    errors.push('No components in runtime manifest')
  }
  for (const comp of manifest.components) {
    if (!comp.id) errors.push('Component with empty id')
    if (!comp.exportName) errors.push(`Component "${comp.id}" has empty exportName`)
    if (!comp.importPath) errors.push(`Component "${comp.id}" has empty importPath`)
  }
  return errors.length === 0 ? { ok: true } : { ok: false, errors }
}

export async function readCoreRuntimeManifest(
  rootPath: string
): Promise<CoreRuntimeManifest | null> {
  if (!IS_TAURI) return null
  const raw = await readText(join(rootPath, 'core', 'runtime-manifest.json'))
  if (!raw) return null
  try {
    const parsed = JSON.parse(raw) as CoreRuntimeManifest
    const validation = validateCoreRuntimeManifest(parsed)
    return validation.ok ? parsed : null
  } catch {
    return null
  }
}

/**
 * Export Core Library as TSX files to <root>/core/.
 * Creates:
 *   core/<Name>.tsx   — re-export from PrimeReact + rules as jsdoc
 *   core/index.ts     — barrel re-exports
 *   core/README.md    — component table + rules
 */
export async function exportCoreComponents(
  rootPath: string,
  components: CoreComponentSpec[]
): Promise<void> {
  if (!IS_TAURI) return
  const coreDir = join(rootPath, 'core')
  await ensureDir(coreDir)

  for (const comp of components) {
    const rulesLines = comp.rules
      ? comp.rules
          .split('\n')
          .filter(Boolean)
          .map((l) => ` * ${l}`)
          .join('\n')
      : ' * (нет правил)'

    const content = [
      `// Generated by Береста Core Library — DO NOT EDIT`,
      `// Source: library/components/${comp.id}.md`,
      `//`,
      `// ${comp.description}`,
      `//`,
      `// Rules:`,
      rulesLines,
      ``,
      `export { ${comp.primeReactExport} } from '${comp.primeReactImport}'`,
      `export type { ${comp.primeReactExport}Props } from '${comp.primeReactImport}'`,
      ``
    ].join('\n')

    await writeText(join(coreDir, `${comp.primeReactExport}.tsx`), content)
  }

  // index.ts
  const indexLines = [
    `// Generated by Береста Core Library — DO NOT EDIT`,
    ``,
    ...components.map((c) => `export { ${c.primeReactExport} } from './${c.primeReactExport}'`),
    ``
  ]
  await writeText(join(coreDir, 'index.ts'), indexLines.join('\n'))

  // README.md
  const tableRows = components.map(
    (c) => `| \`${c.name}\` | \`${c.primeReactImport}\` | ${c.description} |`
  )
  const readmeLines = [
    `# Core Library`,
    ``,
    `> Generated by Береста. Source of truth: \`library/components/\``,
    ``,
    `## Компоненты`,
    ``,
    `| Компонент | PrimeReact | Описание |`,
    `|-----------|-----------|----------|`,
    ...tableRows,
    ``,
    `## Правила использования`,
    ``,
    ...components.flatMap((c) => [
      `### ${c.name}`,
      ``,
      ...(c.rules ? c.rules.split('\n').filter(Boolean) : ['(нет правил)']),
      ``
    ])
  ]
  await writeText(join(coreDir, 'README.md'), readmeLines.join('\n'))

  // runtime-manifest.json
  const runtimeManifest = buildCoreRuntimeManifest(components)
  await writeText(join(coreDir, 'runtime-manifest.json'), JSON.stringify(runtimeManifest, null, 2))
}

